# 2D Lists = a list of lists

cat = ["Balinese", "Cyprus", "Foldex"]
dog = ["Afador", "Cavador", "Dorgi"]
gender = ["Male", "Female"]

# We will add all list into one lists

# This will group all element of individual variable into one variable
pet = [cat, dog, gender]

# To access the 2d lists
print(pet[0][2]) 
# First index is refering to our first list which is cat
# Second index to access element in list that we choose in first index